package gov.test;

class PrimitivesTest {
    public static void main(String[] args) {
        int age = 56;
        System.out.println("Age is " + age);

        long population = 8_000_000_000L;
        System.out.println(" The populatio is " + population);

        double price = 9.95;
        System.out.println("The price is "+ price);

        boolean isSunny = true;
        System.out.println("isSunny is " + true);

        char size = 'S';
        System.out.println("The size is " + size);

        String grade = "A";
        System.out.println("Grade is " + grade);

        // This is how you create a line break
        System.out.println("Roses are red and\nViolets are blue!");

        // This how you would incorporate a double quote in a string
        System.out.println("My favorite color is \"blue\"");

    }
}
